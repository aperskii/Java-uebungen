public class Animal {

    private String name;
    private String art;
    private int age;

    public Animal(){

    }

    public String getName() {
        return name;
    }

    public String getArt() {
        return art;
    }

    public int getAge() {
        return age;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setArt(String art) {
        this.art = art;
    }
}